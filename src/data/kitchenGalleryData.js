// Helper function to get images by category
export const getImagesByCategory = (category) => {
  if (category === 'All') return kitchenGalleryData;
  return kitchenGalleryData.filter(item => item.category === category);
};

// Helper function to get all categories
export const getAllCategories = () => {
  return ['All', ...new Set(kitchenGalleryData.map(item => item.category))];
};

// Helper function to get category count
export const getCategoryCount = (category) => {
  if (category === 'All') return kitchenGalleryData.length;
  return kitchenGalleryData.filter(item => item.category === category).length;
};