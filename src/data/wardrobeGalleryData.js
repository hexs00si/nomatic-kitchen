// Helper function to get images by category for wardrobes
export const getWardrobeImagesByCategory = (category) => {
  if (category === 'All') return wardrobeGalleryData;
  return wardrobeGalleryData.filter(item => item.category === category);
};

// Helper function to get all wardrobe categories
export const getAllWardrobeCategories = () => {
  return ['All', ...new Set(wardrobeGalleryData.map(item => item.category))];
};

// Helper function to get wardrobe category count
export const getWardrobeCategoryCount = (category) => {
  if (category === 'All') return wardrobeGalleryData.length;
  return wardrobeGalleryData.filter(item => item.category === category).length;
};